def main() -> None:
    assert len(RECORDS) == 200, f"레코드 수가 200이 아닙니다: {len(RECORDS)}"

    wb = Workbook()
    ws = wb.active
    ws.title = "개발자전생기록"

    header_font = Font(name="Malgun Gothic", bold=True, size=11, color="C9D1D9")
    header_fill = PatternFill("solid", fgColor="161B22")
    body_font = Font(name="Malgun Gothic", size=10)
    thin = Side(style="thin", color="CCCCCC")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)

    for col, title in enumerate(HEADERS, start=1):
        c = ws.cell(row=1, column=col, value=title)
        c.font = header_font
        c.fill = header_fill
        c.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        c.border = border

    for i, (dev, era, programs, variables, death, achievement, memory) in enumerate(
        RECORDS, start=1
    ):
        row = [i, dev, era, programs, variables, death, achievement, memory]
        for col, value in enumerate(row, start=1):
            c = ws.cell(row=i + 1, column=col, value=value)
            c.font = body_font
            c.alignment = Alignment(vertical="top", wrap_text=(col > 1))
            c.border = border

    widths = [6, 32, 24, 38, 34, 46, 46, 50]
    for col, w in enumerate(widths, start=1):
        ws.column_dimensions[chr(64 + col)].width = w
    ws.freeze_panes = "A2"

    out = os.path.join(os.path.dirname(__file__), "..", "data", "dev-past-lives.xlsx")
    out = os.path.abspath(out)
    os.makedirs(os.path.dirname(out), exist_ok=True)
    wb.save(out)
    print(f"저장 완료: {out} ({len(RECORDS)}건)")


if __name__ == "__main__":
    main()
