HEADERS = ["번호", "전생의 직업 또는 존재", "시대", "사인(죽은 이유)",
           "전생의 업적", "사람들은 전생의 나를 어떻게 기억하고 있는지"]


def main() -> None:
    assert len(RECORDS) == 150, f"레코드 수가 150이 아닙니다: {len(RECORDS)}"

    wb = Workbook()
    ws = wb.active
    ws.title = "전생기록"

    header_font = Font(name="Malgun Gothic", bold=True, size=11, color="FFFFFF")
    header_fill = PatternFill("solid", fgColor="4B3B8F")
    body_font = Font(name="Malgun Gothic", size=10)
    thin = Side(style="thin", color="CCCCCC")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)

    for col, title in enumerate(HEADERS, start=1):
        c = ws.cell(row=1, column=col, value=title)
        c.font = header_font
        c.fill = header_fill
        c.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        c.border = border

    for i, (being, era, death, achievement, memory) in enumerate(RECORDS, start=1):
        row = [i, being, era, death, achievement, memory]
        for col, value in enumerate(row, start=1):
            c = ws.cell(row=i + 1, column=col, value=value)
            c.font = body_font
            c.alignment = Alignment(vertical="top", wrap_text=(col > 1))
            c.border = border

    widths = [6, 30, 28, 44, 46, 52]
    for col, w in enumerate(widths, start=1):
        ws.column_dimensions[chr(64 + col)].width = w
    ws.freeze_panes = "A2"

    out = os.path.join(os.path.dirname(__file__), "..", "data", "past-lives.xlsx")
    out = os.path.abspath(out)
    os.makedirs(os.path.dirname(out), exist_ok=True)
    wb.save(out)
    print(f"저장 완료: {out} ({len(RECORDS)}건)")


if __name__ == "__main__":
    main()
